package Arrays;

public class ArrayBasics {
    public static void main(String[] args) {
        
        int num[] = {5, 7, 8 };
        num[2] = 9;

        System.out.println(num[2]);

        int nums[] = new int[4];
        nums[0] = 4;
        nums[1] = 8;
        nums[2] = 12;
        nums[3] = 16;

        System.out.println(nums[1]);

    }
    
}
